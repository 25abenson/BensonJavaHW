public class PartA {
    public static void main(String[] args) {

        // pi is an example of a static constant of the math class
        final double PI = Math.PI;
        // print out pi value
        System.out.println("Pi = " + PI);

        // the round method is a static method of the Math class
        double piRounded = Math.round(PI);
        // print out rounded pi value
        System.out.println("Pi (rounded) = " + piRounded);

    }
}